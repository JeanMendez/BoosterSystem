        <h1>Importancia de las Actividades Extracurriculares</h1>
    </div>    
    <div class="texto">
        <p>
            La escuela solo forma una pequeña parte de la educación integral de un ser humano. Una persona puede adquirir conocimientos y mejorar sus habilidades en muchos contextos ajenos a la educación formal. Las personas estamos aprendiendo todo el tiempo, pero en muchas ocasiones, necesitamos de una guía que nos facilite el desarrollo de ciertas tareas o el cumplimiento de metas. Para esto existen las actividades extracurriculares.
            Las actividades extracurriculares son programas o proyectos diseñados para el aprendizaje o el desarrollo de habilidades, y no forman parte de los programas académicos formales. Es decir, no forman parte de las materias oficiales, ni cuentan para calificar el desempeño de un alumno dentro de la escuela. De hecho, la mayoría de las actividades extracurriculares se imparten fuera de ella.
            Tener actividades adicionales a la formación escolar es importante, tanto para distraerse y divertirse, como para adquirir nuevas habilidades o conocimientos.
        </p>
        <br>
        <h3>Mejora de autoestima y salud emocional</h3>
        <p> 
            Una actividad extracurricular, siempre que se elija bien, tiene beneficios directos sobre la salud emocional por varias razones. Por un lado, está la sensación de logro: una persona que realiza una actividad acorde con sus gustos y ve su avance, puede sentirse orgullosa de lo que está consiguiendo. Por otro lado, el hecho de que conviva con otras personas con gustos similares reafirma su identidad y puede proporcionarle seguridad.
        </p>
